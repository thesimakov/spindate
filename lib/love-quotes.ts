/** Одна цитата на календарный день (одинаковая для всех за этот день) */
export function getDailyLoveQuote(date: Date = new Date()): LoveQuote {
  const dateKey = date.toISOString().slice(0, 10)
  const seed = dateKey.split("").reduce((acc, ch) => acc + ch.charCodeAt(0), 0)
  return LOVE_QUOTES[seed % LOVE_QUOTES.length]!
}
